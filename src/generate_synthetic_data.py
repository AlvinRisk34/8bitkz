def fill_template(template: str) -> str:
    return template.format(
        floor=RNG.integers(1, 9),
        street=RNG.choice(STREETS),
    )


def generate_appeals(n=N_APPEALS):
    rows = []
    start_date = datetime(2025, 1, 1)

    for i in range(n):
        category = RNG.choice(list(CATEGORIES.keys()))
        # низкая срочность встречается чаще, высокая — реже (как в жизни)
        urgency_tier = RNG.choice(["low", "medium", "high"], p=[0.55, 0.32, 0.13])
        template = RNG.choice(CATEGORIES[category][urgency_tier])
        text = fill_template(template)

        district = RNG.choice(DISTRICTS)
        date = start_date + timedelta(days=int(RNG.integers(0, 300)))

        # повторные обращения по похожей проблеме - больше у "средних/высоких"
        base_repeat = {"low": 0.3, "medium": 1.2, "high": 2.5}[urgency_tier]
        repeat_count = RNG.poisson(base_repeat)

        # истинная срочность: комбинация уровня шаблона + повторности + шум
        urgency_score = {"low": 0.05, "medium": 0.35, "high": 0.75}[urgency_tier]
        urgency_score += repeat_count * 0.05
        urgency_score += RNG.normal(0, 0.07)
        urgency_score = np.clip(urgency_score, 0, 1)
        is_urgent = int(RNG.binomial(1, urgency_score))

        rows.append(
            {
                "appeal_id": i + 1,
                "date": date.date(),
                "district": district,
                "category": category,
                "text": text,
                "repeat_count": repeat_count,
                "true_tier": urgency_tier,  # служебное поле, для анализа/аудита
                "is_urgent": is_urgent,
            }
        )

    return pd.DataFrame(rows)


def main():
    df = generate_appeals()
    df.to_csv("data/raw/appeals.csv", index=False)
    print(f"Сгенерировано обращений: {len(df)}")
    print(df["category"].value_counts())
    print(f"Доля срочных: {df['is_urgent'].mean():.2%}")


if __name__ == "__main__":
    main()
